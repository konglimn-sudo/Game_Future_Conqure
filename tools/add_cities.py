path = os.path.join(os.path.dirname(__file__), "..", "game", "data", "world.json")
world = json.load(open(path, encoding="utf8"))

region_polys = []  # (region_id, shapely Polygon)
for r in world["regions"]:
    for ring in r["polys"]:
        if len(ring) >= 4:
            region_polys.append((r["id"], Polygon(ring)))

cities, misses = [], []
for name, lon, lat, tier in CITIES:
    p = Point(*to_px(lon, lat))
    rid = next((i for i, poly in region_polys if poly.contains(p)), None)
    if rid is None:  # 落海/落在被并入背景的微型国 → 最近区域
        rid, d = min(((i, poly.distance(p)) for i, poly in region_polys), key=lambda t: t[1])
        if d > 6 * SCALE:  # 离谱的不挂
            misses.append(name)
            continue
    cities.append({"name": name, "x": round(p.x, 1), "y": round(p.y, 1),
                   "tier": tier, "region": rid})

world["cities"] = cities
json.dump(world, open(path, "w", encoding="utf8"), ensure_ascii=False)
by_region = {}
for c in cities:
    by_region.setdefault(c["region"], []).append(c["name"])
print(f"cities={len(cities)} 未挂载={misses}")
print("样例归属:", {world['regions'][k]['name']: v for k, v in list(by_region.items())[:6]})
